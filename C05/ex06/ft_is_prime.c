int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 2)
		return (0);
	if (nb < 4)
		return (1);
	i = 1;
	while (i * i < nb && i * i > 0)
		if (!(nb % ++i))
			return (0);
	return (1);
}
