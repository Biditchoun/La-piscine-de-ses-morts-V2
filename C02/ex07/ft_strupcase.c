char	*ft_strupcase(char *str)
{
	int	i;

	if (!str)
		return (str);
	i = -1;
	while (str[++i])
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] += 'A' - 'a';
	return (str);
}
