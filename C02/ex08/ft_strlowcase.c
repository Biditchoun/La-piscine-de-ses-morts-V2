char	*ft_strlowcase(char *str)
{
	int	i;

	if (!str)
		return (str);
	i = -1;
	while (str[++i])
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 'a' - 'A';
	return (str);
}
