char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;
	int	k;

	i = -1;
	while (str[++i])
	{
		k = i;
		j = 0;
		while (str[k++] == to_find[j++])
			if (!to_find[j])
				return (&str[i]);
	}
	return (0);
}
