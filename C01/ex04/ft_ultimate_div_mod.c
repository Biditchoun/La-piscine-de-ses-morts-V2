void	ft_ultimate_div_mod(int *a, int *b)
{
	int	buf;
	int	buf2;

	if (!a || !b || !*b)
		return ;
	buf = *a / *b;
	buf2 = *a % *b;
	*a = buf;
	*b = buf2;
}
